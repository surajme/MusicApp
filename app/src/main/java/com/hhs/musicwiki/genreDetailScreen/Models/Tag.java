package com.hhs.musicwiki.genreDetailScreen.Models;

public class Tag {
    private String name;
    private long total;
    private long reach;
    private Wiki wiki;

    public String getName() {
        return name;
    }

    public long getTotal() {
        return total;
    }

    public long getReach() {
        return reach;
    }

    public Wiki getWiki() {
        return wiki;
    }
}
